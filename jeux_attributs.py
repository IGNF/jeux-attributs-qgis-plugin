    @staticmethod
    def clear_layout(layout):
        """Supprime tous les widgets d’un QLayout"""
        while layout.count():
            item = layout.takeAt(0)
            widget = item.widget()
            if widget is not None:
                widget.setParent(None)
                widget.deleteLater()
            elif item.layout():
                JeuxAttributs.clear_layout(item.layout())

